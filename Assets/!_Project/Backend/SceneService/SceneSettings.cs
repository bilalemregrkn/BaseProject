using System.Collections.Generic;
using Plexitugins.SceneService;
using Sirenix.OdinInspector;
using UnityEngine;

namespace Plugins.SceneService
{
    [CreateAssetMenu(menuName = "Game/Scene Settings", fileName = "SceneSettings")]
    public class SceneSettings : ScriptableObject
    {
        [SerializeField] [InlineEditor] private List<BaseScene> _scenes = new();

        public IReadOnlyList<BaseScene> Scenes => _scenes;

        public BaseScene GetScene(string id)
        {
            foreach (var scene in _scenes)
                if (scene.Id == id) return scene;
            return null;
        }

#if UNITY_EDITOR
        [Button]
        private void Refresh()
        {
            _scenes.Clear();

            var guids = UnityEditor.AssetDatabase.FindAssets("t:BaseScene");
            foreach (var guid in guids)
            {
                var path = UnityEditor.AssetDatabase.GUIDToAssetPath(guid);
                var asset = UnityEditor.AssetDatabase.LoadAssetAtPath<BaseScene>(path);
                if (asset != null)
                    _scenes.Add(asset);
            }

            UnityEditor.EditorUtility.SetDirty(this);
            UnityEditor.AssetDatabase.SaveAssetIfDirty(this);

            RegenerateSceneTypeFile();

            Debug.Log($"[SceneSettings] Refreshed {_scenes.Count} BaseScene asset(s).");
        }

        private void RegenerateSceneTypeFile()
        {
            var sb = new System.Text.StringBuilder();
            sb.AppendLine("// THIS CLASS IS AUTOGENERATED");
            sb.AppendLine("namespace Plugins.SceneService");
            sb.AppendLine("{");
            sb.AppendLine("    public static class SceneType");
            sb.AppendLine("    {");
            foreach (var scene in _scenes)
            {
                var raw        = System.Text.RegularExpressions.Regex.Replace(scene.Id, @"[^a-zA-Z0-9_]", "_");
                var identifier = char.ToUpper(raw[0]) + raw.Substring(1);
                sb.AppendLine($"        public const string {identifier} = \"{scene.Id}\";");
            }
            sb.AppendLine("    }");
            sb.Append("}");

            const string assetPath = "Assets/!_Project/Backend/SceneService/SceneType.cs";
            System.IO.File.WriteAllText(
                System.IO.Path.Combine(UnityEngine.Application.dataPath, "../" + assetPath),
                sb.ToString()
            );
            UnityEditor.AssetDatabase.ImportAsset(assetPath);
        }
#endif
    }
}
